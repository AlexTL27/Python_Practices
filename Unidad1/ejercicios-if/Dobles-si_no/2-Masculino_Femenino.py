print("aplicación que que el usuario introduzca ‘F’ o ‘M’ y que imprima Masculino si es ‘M’ de lo contrario mostrará el mensaje de Femenino.")


car = input("Indica F para femenino o M para masculino:  ")

if car == "M":
    print("Masculino")
elif car == "F":
    print("Femenino")
else:
    print("Ningún dato coincide")